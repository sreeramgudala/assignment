package com.cg.web;

public class ErrorInfo {
	private String message;

	public ErrorInfo(String message) {
		super();
		this.message = message;
	}

	public ErrorInfo() {
		
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	

}
